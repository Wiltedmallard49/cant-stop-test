// Checking if the dragon goes above or below the screen
game.onUpdate(function on_update() {
    let y = dragon.y
    if (y > scene.screenHeight()) {
        die()
    } else if (y < 0) {
        dragon.y = 0
    }
    
})
// defining the die function
function die() {
    info.changeLifeBy(-1)
    dragon.y = scene.screenHeight() / 2
    dragon.vy = 0
}

